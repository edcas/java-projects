package com.example.introduction.utils;

public class Calculator {

    public double sum(double number, double otherNumber) {
        return number + otherNumber;
    }

    public double subtract(double number, double otherNumber) {
        return number - otherNumber;
    }

    public double multiply(double number, double otherNumber) {
        return number + otherNumber;
    }

    public double divide(double number, double otherNumber) {
        return number / otherNumber;
    }
}
